def get_country_info(flag_key: str) -> tuple[str | None, list[float] | None]:
    """Return (iso_code, [lat, lon]) for a normalized flag_key, or (None, None)."""
    entry = COUNTRY_LOOKUP.get(flag_key)
    if entry:
        return entry[0], entry[1]
    return None, None
